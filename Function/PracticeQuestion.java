public class PracticeQuestion{
    //01-- Average of three number method
    public static double AverageNumber(int a, int b, int c){
        return (a + b + c) / 3.0;
    }

    // Math Functions 

    public static void main(String[] args) {
        System.out.println(AverageNumber(100, 100, 100));
    }
}